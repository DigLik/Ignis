    /// <summary>Отрисовывает текст на экране.</summary>
    /// <param name="t">Строка текста.</param>
    /// <param name="p">Левый верхний угол первой буквы в мировых координатах.</param>
    /// <param name="s">Высота шрифта в мировых единицах.</param>
    /// <param name="c">Цвет текста.</param>
    public void DrawText(string t, Vector2 p, float s, Color c)
    {
        if (string.IsNullOrEmpty(t)) return;

        float charSize = s;
        float pixelSize = charSize / 8f;
        float cursorX = p.X;
        float spacing = pixelSize; // 1 пиксель пустого пространства между символами

        for (int charIdx = 0; charIdx < t.Length; charIdx++)
        {
            char ch = t[charIdx];
            if (char.IsWhiteSpace(ch))
            {
                float whitespaceWidth = (ch == '\t') ? (8f * pixelSize) : (4f * pixelSize);
                cursorX += whitespaceWidth + spacing;
                continue;
            }

            int fontIdx = ch - 32;
            if (fontIdx < 0 || fontIdx >= 96)
                fontIdx = 95; // Unknown to block/DEL char

            // Находим фактические границы символа по горизонтали
            int minCol = 8;
            int maxCol = -1;
            bool hasPixels = false;

            for (int r = 0; r < 8; r++)
            {
                byte rowByte = FontData[fontIdx * 8 + r];
                if (rowByte == 0) continue;

                for (int col = 0; col < 8; col++)
                {
                    if ((rowByte & (1 << (7 - col))) != 0)
                    {
                        hasPixels = true;
                        if (col < minCol) minCol = col;
                        if (col > maxCol) maxCol = col;
                    }
                }
            }

            float charWidth;
            if (hasPixels)
            {
                charWidth = (maxCol - minCol + 1) * pixelSize;

                _batcher.Add(new GPUShape
                {
                    Type = 5,
                    P1 = new Vector2(cursorX + charWidth * 0.5f, p.Y + charSize * 0.5f),
                    P2 = new Vector2(charWidth, charSize),
                    Angle = fontIdx,
                    Thickness = minCol,
                    Color = c.ToVector4()
                });

                cursorX += charWidth + spacing;
            }
            else
            {
                // Для пробела и символов без пикселей
                charWidth = (ch == ' ') ? (4f * pixelSize) : (2f * pixelSize);
                cursorX += charWidth + spacing;
            }
        }
    }

    /// <summary>Измеряет ширину текста при заданном размере шрифта.</summary>
    /// <param name="t">Строка текста для измерения.</param>
    /// <param name="s">Высота шрифта в мировых единицах.</param>
    /// <returns>Ширина текста в мировых единицах.</returns>
    public static float MeasureText(string t, float s)
    {
        if (string.IsNullOrEmpty(t)) return 0f;

        float charSize = s;
        float pixelSize = charSize / 8f;
        float width = 0f;
        float spacing = pixelSize;

        for (int charIdx = 0; charIdx < t.Length; charIdx++)
        {
            char ch = t[charIdx];
            if (char.IsWhiteSpace(ch))
            {
                float whitespaceWidth = (ch == '\t') ? (8f * pixelSize) : (4f * pixelSize);
                width += whitespaceWidth + spacing;
                continue;
            }

            int fontIdx = ch - 32;
            if (fontIdx < 0 || fontIdx >= 96)
                fontIdx = 95; // Unknown to block/DEL char

            int minCol = 8;
            int maxCol = -1;
            bool hasPixels = false;

            for (int r = 0; r < 8; r++)
            {
                byte rowByte = FontData[fontIdx * 8 + r];
                if (rowByte == 0) continue;

                for (int col = 0; col < 8; col++)
                {
                    if ((rowByte & (1 << (7 - col))) != 0)
                    {
                        hasPixels = true;
                        if (col < minCol) minCol = col;
                        if (col > maxCol) maxCol = col;
                    }
                }
            }

            float charWidth;
            if (hasPixels)
            {
                charWidth = (maxCol - minCol + 1) * pixelSize;
            }
            else
            {
                charWidth = (ch == ' ') ? (4f * pixelSize) : (2f * pixelSize);
            }

            width += charWidth + spacing;
        }

        if (width > 0f)
            width -= spacing;

        return width;
    }

    /// <summary>Обновляет параметры ортографической камеры рендерера.</summary>
    /// <param name="p">Положение камеры (центр видимой области) в мировом пространстве.</param>
    /// <param name="b">Размеры видимой области (ширина и высота) камеры в мировом пространстве.</param>
    public void UpdateCamera(Vector2 p, Vector2 b)
    {
        _cameraPosition = p;

        float windowWidth = _window.Size.X;
        float windowHeight = _window.Size.Y;
        if (windowWidth <= 0 || windowHeight <= 0 || b.X <= 0 || b.Y <= 0)
        {
            _cameraSize = b;
            return;
        }

        float windowAspect = windowWidth / windowHeight;
        float targetAspect = b.X / b.Y;

        if (windowAspect > targetAspect)
        {
            _cameraSize = new Vector2(b.Y * windowAspect, b.Y);
        }
        else
        {
            _cameraSize = new Vector2(b.X, b.X / windowAspect);
        }
    }

